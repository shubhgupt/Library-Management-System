    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            int bid = Integer.parseInt(id.getText());
            String bname = name.getText();
            String bedition = (String)edition.getSelectedItem();
            String bauthor = author.getText();
            String bpublisher = publisher.getText();
            int bpages = Integer.parseInt(pages.getText());
            float bprice = Float.parseFloat(price.getText());
            
            
            if(bid <= 0 || bname.isEmpty()|| bedition.isEmpty() ||bauthor.isEmpty() || bpublisher.isEmpty() || bpages == 0 || bprice <= 0 || bedition.equals("Select Edition"))
            {
                JOptionPane.showMessageDialog(rootPane, " Please Fill all the Fields! \n Make Sure All Inputs should be valid!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String query = "Insert Into books (book_id,name,edition,author,publisher,pages,price) values(?,?,?,?,?,?,?)";
                pst = con.prepareStatement(query);
                pst.setInt(1, bid);
                pst.setString(2, bname);
                pst.setString(3,bedition);
                pst.setString(4, bauthor);
                pst.setString(5, bpublisher);
                pst.setInt(6, bpages);
                pst.setFloat(7,bprice);
                
                int result = pst.executeUpdate();
                if(result != 0)
                {
               
                    JOptionPane.showMessageDialog(rootPane, "New Book Inserted Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    pst.close();
                    clearAllFields();
                }
            }
        }
        catch(com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException e)
        {
            JOptionPane.showMessageDialog(rootPane,"This Book ID is Already Registered!","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Make Sure Price and Pages are Valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

