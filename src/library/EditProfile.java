    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String username = uname.getText();
            String pass = String.valueOf(pwd.getPassword());
            String npass = String.valueOf(npwd.getPassword());
            String cnpass = String.valueOf(cnpwd.getPassword());
            
            
            if(username.isEmpty() || pass.isEmpty() || npass.isEmpty() || cnpass.isEmpty())
                JOptionPane.showMessageDialog(rootPane, "Please Fill All The Fields First!", "Warning", JOptionPane.WARNING_MESSAGE);
            else{
                
                if(npass.equals(cnpass) == false)
                    JOptionPane.showMessageDialog(rootPane, "New Password And Confirm Password Does Not Match!", "Error", JOptionPane.ERROR_MESSAGE);

                else{
                    int password = pass.hashCode();
                    String query = "Select * from account where username = ? And password = ?";
                            
                    
                    PreparedStatement st = con.prepareStatement(query);
                    st.setString(1,username);
                    st.setInt(2,password);
                    rs = st.executeQuery();
                    if(rs.next())
                    {
                        int newpass = cnpass.hashCode();
                        String change = "Update account SET password = ? where username = ? ";
                        PreparedStatement st2 = con.prepareStatement(change);
                        st2.setInt(1,newpass);
                        st2.setString(2,rs.getString("username"));
                        
                        
                        int result = st2.executeUpdate();
                        if(result != 0)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Pssword Updated Suceesfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                            st2.close();
                            st.close();
                            dispose();
                        }
                    }
                    else
                        JOptionPane.showMessageDialog(rootPane, "Username or Password is Incorrect!", "Error", JOptionPane.ERROR_MESSAGE);
                }    
                
            }
                
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

