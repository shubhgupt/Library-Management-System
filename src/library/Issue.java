    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String bid = bookid.getText();
            if(bid.isEmpty())
            {
                JOptionPane.showMessageDialog(rootPane, "Fill the Book ID First!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else{
                int bkid = Integer.parseInt(bid);
                
                String query = "Select * from books Where book_id = "+bkid;
                Statement st = con.createStatement();
                rs = st.executeQuery(query);
                if(rs.next())
                {
                     bname.setText(rs.getString("name"));
                     bedition.setText(rs.getString("edition"));
                     bauthor.setText(rs.getString("author"));
                     bpublisher.setText(rs.getString("publisher"));
                     String pages = ""+rs.getInt("pages");
                     bpages.setText(pages);
                     String price = ""+rs.getFloat("price");
                     bprice.setText(price);
                     
                     st.close();
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "This Book ID is not Present in Database!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

