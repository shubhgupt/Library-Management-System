    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            String stname = sname.getText();
            String bkname = bname.getText();
            if(stname.isEmpty()||bkname.isEmpty())
                JOptionPane.showMessageDialog(rootPane, "Please Fill All The Fields First!", "Error", JOptionPane.ERROR_MESSAGE);

            else
            {
                int sid = Integer.parseInt(studid.getText());
                int bid = Integer.parseInt(bookid.getText());
                
                String bookcheck = "Select id from book_issued where Book_id ="+bid;
                Statement st = con.createStatement();
                ResultSet Check = st.executeQuery(bookcheck);
                if(Check.next())
                {
                    JOptionPane.showMessageDialog(rootPane, "This Book is Issued Already!", "Error", JOptionPane.ERROR_MESSAGE);
                    clearAllFields();
                }
                else{
                    int noOfBooksIssued = 0;

                    String checkQuery = "Select id from book_issued where Student_id = "+sid;
                    Statement st1 = con.createStatement();
                    rs = st1.executeQuery(checkQuery);

                    while(rs.next())
                        noOfBooksIssued++ ;
                    rs.close();
                    if(noOfBooksIssued <3)
                    {
                        String query = "Insert into book_issued (Student_id,Book_id,Date_of_Issue) values(?,?,?)";

                        PreparedStatement pst = con.prepareCall(query);
                        pst.setInt(1,sid);
                        pst.setInt(2,bid);
                        pst.setString(3,((JTextField)doi.getDateEditor().getUiComponent()).getText());

                        int result = pst.executeUpdate();
                        if(result != 0)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Book Issued Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                            pst.close();
                            clearAllFields();
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane,"Warnings: \n This Student has already Issued 3 books! \n First Return a Book then try to Issue another One!","Error",JOptionPane.ERROR_MESSAGE);
                        clearAllFields();
                    }
                }
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

