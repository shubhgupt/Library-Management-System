    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String sroll = roll.getText();
            String sname = name.getText();
            String sfname = fname.getText();
            String sbranch = (String)branch.getSelectedItem();
            String ssem = (String)sem.getSelectedItem();
            String smob = number.getText();
            String sadd = address.getText();
            
            String regex = "(0/91)?[6-9][0-9]{9}";
            if(sroll.isEmpty()||sname.isEmpty()||sfname.isEmpty()||smob.matches(regex) == false || sadd.isEmpty())
            {
                JOptionPane.showMessageDialog(rootPane,"Make Sure Following Points: \n  -> All Fields are Required! \n  -> All Inputs Should be Valid!","Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                long mob = Long.parseLong(smob);
                String query = "Insert into students (Roll_no,Name,Father_name,Branch,Semester,Mobile_Number,Address) values(?,?,?,?,?,?,?)";
                pst = con.prepareStatement(query);
                pst.setString(1, sroll);
                pst.setString(2, sname);
                pst.setString(3, sfname);
                pst.setString(4, sbranch);
                pst.setString(5, ssem);
                pst.setLong(6, mob);
                pst.setString(7, sadd);
                
                int result = pst.executeUpdate();
                if(result != 0)
                {
                    JOptionPane.showMessageDialog(rootPane,"New Student Added Successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
                    pst.close();
                    clearAllFields();
                }
            }    
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

