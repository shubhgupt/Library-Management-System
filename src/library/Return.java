    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String sid = studid.getText();
            if(sid.isEmpty())
            {
                JOptionPane.showMessageDialog(rootPane, "Please fill the Student ID first!", "Error", JOptionPane.ERROR_MESSAGE);
                clearAllFields();
            }
            else
            {
                int stid = Integer.parseInt(sid);
                
                String query = "Select * from students where student_id ="+stid;
                Statement st = con.createStatement();
                rs = st.executeQuery(query);
                if(rs.next())
                {
                    sroll.setText(rs.getString("Roll_no"));
                    sname.setText(rs.getString("Name"));
                    sfname.setText(rs.getString("Father_name"));
                    sbranch.setText(rs.getString("Branch"));
                    ssem.setText(rs.getString("Semester"));
                    smob.setText(""+rs.getLong("Mobile_Number"));
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane, "This Student ID is not present in Database!", "Error", JOptionPane.ERROR_MESSAGE);
                    clearAllFields();
                }
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

