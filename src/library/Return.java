    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            String check = sname.getText();
            if(check.isEmpty())
                JOptionPane.showMessageDialog(rootPane, "Please Fill The Student Details First!", "Error", JOptionPane.ERROR_MESSAGE);
            else
            {
                int sid = Integer.parseInt(studid.getText());
                
                String query = "Select Book_id from book_issued where Student_id ="+sid;
                Statement st = con.createStatement();
                rs = st.executeQuery(query);
                
                if(rs.next())
                {
                    int row = 0;
                    boolean condition = true;
                    while(condition)
                    {
                        String sql = "Select * from books where book_id = "+rs.getInt(1);
                        Statement st1 = con.createStatement();
                        ResultSet bookInfo = st1.executeQuery(sql);
                        bookInfo.next();
                        bookTab.setValueAt(bookInfo.getInt("book_id"),row,0);
                        bookTab.setValueAt(bookInfo.getString("name"),row,1);
                        bookTab.setValueAt(bookInfo.getString("edition"),row,2);
                        bookTab.setValueAt(bookInfo.getString("author"),row,3);
                        bookTab.setValueAt(bookInfo.getString("publisher"),row,4);
                        bookTab.setValueAt(bookInfo.getInt("pages"),row,5);
                        bookTab.setValueAt(bookInfo.getFloat("price"),row,6);
                        row++;
                        condition = rs.next();
                    }
                    st.close();
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "There is No Book Issued by this Student!", "Warning", JOptionPane.WARNING_MESSAGE);

            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            String check = sname.getText();
            if(check.isEmpty())
                JOptionPane.showMessageDialog(rootPane, "Please Fill the Student Details First!", "Error", JOptionPane.ERROR_MESSAGE);
            else
            {
                int sid = Integer.parseInt(studid.getText());
                String query = "Select Book_id from book_issued where Student_id = "+sid;
                Statement st = con.createStatement();
                ResultSet rs2 = st.executeQuery(query);
               
                
                boolean noOfBooksSelected[] = new boolean[]{book1.isSelected(),book2.isSelected(),book3.isSelected()};
                
                int noOfBooksReturned = 0;
                for (int i = 0; i < 3; i++) {
                    if(rs2.next() == false)
                        break;
                    if(noOfBooksSelected[i])
                    {
                        String deleteQuery = "Delete From book_issued where Book_id = "+rs2.getInt(1);
                        Statement st1 = con.createStatement();
                        int result = st1.executeUpdate(deleteQuery);
                        
                        if(result != 0)
                            noOfBooksReturned++;
                    }
                }
                if(noOfBooksReturned > 0 )
                {
                    JOptionPane.showMessageDialog(rootPane, noOfBooksReturned+" Book/s Returned Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "There is No Book Selected For Return!", "Error", JOptionPane.ERROR_MESSAGE);
                
                st.close();
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

